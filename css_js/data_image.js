function addImageToElement(elementClass, imageUrl) {
    var elements = document.getElementsByClassName(elementClass);
    for (var i = 0; i < elements.length; i++) {
        var image = new Image();
        image.src = imageUrl;
        elements[i].appendChild(image);
    }
}


